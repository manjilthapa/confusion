import React, { useState } from "react";
import { Link } from "react-router-dom";
import {
  Breadcrumb,
  BreadcrumbItem,
  Button,
  Form,
  FormGroup,
  Label,
  Input,
  Col,
} from "reactstrap";

interface Contact {
  firstName: string;
  lastName: string;
  telNum: number;
  email: string;
  agree: boolean;
  contactType: string;
  message: string;
}
